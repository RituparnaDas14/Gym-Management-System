    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        java.sql.Date sd=null;
       if(jTextField2.getText().equals(""))
       {
       JOptionPane.showMessageDialog(null, "please enter Last Name");
       return;
       }
       if(jTextField3.getText().equals(""))
       {
       JOptionPane.showMessageDialog(null, "please enter First Name");
       return;
       }
       if(jTextField4.getText().equals(""))
       {
       JOptionPane.showMessageDialog(null, "please enter amount paid");
       return;
       }
      java.text.SimpleDateFormat sdf= new java.text.SimpleDateFormat("dd-MM-yyyy");
      String dt=sdf.format(jDateChooser1.getDate());
        try {
            java.util.Date d=sdf.parse(dt);
            sd=new java.sql.Date(d.getTime());
            JOptionPane.showMessageDialog(null, sd);
        } catch (Exception e) {
        }
      
         try {
            Connection con=Dbconnection.getConn();
            PreparedStatement ps=con.prepareStatement("insert into receipt(date1,lastname,firstname,amountpaid,paymentfor,howpayed,receivedby) values (?,?,?,?,?,?,?)");
          ps.setDate(1, sd);
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            ps.setString(4, jTextField4.getText());
            ps.setString(5, jComboBox1.getSelectedItem().toString());
             ps.setString(6, jComboBox2.getSelectedItem().toString());
              ps.setString(7, jComboBox3.getSelectedItem().toString());
            
            
            
            int x =ps.executeUpdate();
            if (x>0) {
                JOptionPane.showMessageDialog(null, "successfully inserted");
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "database error");
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed


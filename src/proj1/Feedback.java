    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         java.sql.Date sd=null;
         if(jTextField1.getText().equals(""))
       {
       JOptionPane.showMessageDialog(null, "please enter username");
       return;
       }
      
           if(jTextArea1.getText().equals(""))
       {
       JOptionPane.showMessageDialog(null, "please enter feedback");
       return;
       }
            java.text.SimpleDateFormat sdf= new java.text.SimpleDateFormat("dd-MM-yyyy");
      String dt=sdf.format(jDateChooser1.getDate());
        try {
            java.util.Date d=sdf.parse(dt);
            sd=new java.sql.Date(d.getTime());
            JOptionPane.showMessageDialog(null, sd);
        } catch (Exception e) {
        }
            try {
            Connection con=Dbconnection.getConn();
            PreparedStatement ps=con.prepareStatement("insert into feedback(name,date1,feedback) values (?,?,?)");
            ps.setString(1,jTextField1.getText() );
             ps.setDate(2, sd);
             ps.setString(3, jTextArea1.getText());
            int x =ps.executeUpdate();
            if (x>0) {
                JOptionPane.showMessageDialog(null, "successfully inserted");
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "database error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

